import { stripImageMetadata } from '../image-privacy.util';

describe('stripImageMetadata', () => {
  it('removes JPEG EXIF APP1 payloads', () => {
    const jpeg = Buffer.from([
      0xff, 0xd8,
      0xff, 0xe1, 0x00, 0x0c, 0x45, 0x78, 0x69, 0x66, 0x00, 0x00, 0x41, 0x42, 0x43, 0x44,
      0xff, 0xe0, 0x00, 0x04, 0x00, 0x00,
      0xff, 0xda, 0x00, 0x08, 0x01, 0x01, 0x00, 0x00, 0x3f, 0x00,
      0x11, 0x22, 0x33, 0xff, 0xd9,
    ]);

    const stripped = stripImageMetadata(jpeg, 'image/jpeg');

    expect(stripped.includes(Buffer.from('Exif'))).toBe(false);
    expect(stripped.subarray(0, 2).equals(Buffer.from([0xff, 0xd8]))).toBe(true);
  });

  it('removes PNG metadata chunks', () => {
    const signature = [0x89, 0x50, 0x4e, 0x47, 0x0d, 0x0a, 0x1a, 0x0a];
    const ihdr = [0x00, 0x00, 0x00, 0x00, 0x49, 0x48, 0x44, 0x52, 0x00, 0x00, 0x00, 0x00];
    const text = [0x00, 0x00, 0x00, 0x04, 0x74, 0x45, 0x58, 0x74, 0x41, 0x42, 0x43, 0x44, 0x00, 0x00, 0x00, 0x00];
    const iend = [0x00, 0x00, 0x00, 0x00, 0x49, 0x45, 0x4e, 0x44, 0x00, 0x00, 0x00, 0x00];
    const png = Buffer.from([...signature, ...ihdr, ...text, ...iend]);

    const stripped = stripImageMetadata(png, 'image/png');

    expect(stripped.includes(Buffer.from('tEXt'))).toBe(false);
    expect(stripped.includes(Buffer.from('IHDR'))).toBe(true);
    expect(stripped.includes(Buffer.from('IEND'))).toBe(true);
  });
});